# don't run tests requiring a real internet connection, unless opted into.
if ENV['TEST_ONLINE']
  assert 'HTTP::Session interactive keepalive, with SSL' do
    block_invoked = false
    session = HTTP::Session.new('https://www.example.com')
    session.ssl_options[:ca_chain] = CA_CHAIN_EXAMPLE_COM
    session.when_blocking { |io, timeout| assert_not_nil(io); block_invoked = true }

    rsp = session.get '/'
    assert_equal 200, rsp.status, 'expected 200 OK from server'
    assert_true block_invoked, 'should have called the block while waiting for host response'
    assert_match "*</html>", rsp.body.read.chomp

    rsp = session.get '/missing_want_404'
    assert_equal 404, rsp.status, 'expected 404 Not Found from server'
    assert_true block_invoked, 'should have called the block while waiting for host response'
  end

  assert 'HTTP::Session interactive close, without SSL' do
    session = HTTP::Session.new('http://www.google.com')
    rsp = session.get '/'# do |req|
    #   req['connection'] = 'close'
    # end
    assert_equal 200, rsp.status, 'expected 200 OK from server'
    # assert_true session.connection.closed?, 'connection should have been closed by server'
    assert_match '*</html>', rsp.body.read.chomp

    # obviously have to reopen the closed connection here, which should
    # happen automatically
    rsp = session.get '/missing_want_404'
    assert_equal 404, rsp.status, 'expected 404 Not Found from server'
  end

  assert 'HTTP::Session interactive close, with SSL' do
    session = HTTP::Session.new('https://www.google.com')
    session.ssl_options[:ca_chain] = CA_CHAIN_GOOGLE_COM

    rsp = session.get '/'# do |req|
    #   req['connection'] = 'close'
    # end
    assert_equal 200, rsp.status, 'expected 200 OK from server'
    # assert_true session.closed?, 'connection should have been closed by server'
    assert_match '*</html>', rsp.body.read.chomp

    # obviously have to reopen the closed connection here, which should
    # happen automatically
    rsp = session.get '/missing_want_404'
    assert_equal 404, rsp.status, 'expected 404 Not Found from server'
  end

  assert 'HTTP::Session using polling-based parsing' do
    session = HTTP::Session.new('https://www.example.com')
    session.ssl_options[:ca_chain] = CA_CHAIN_EXAMPLE_COM

    parser = session.get '/', parse: false
    iterations = 0
    iterations += 1 and parser.parse until parser.ready?
    assert_true iterations > 1, 'number of iterations to parse may vary but should be more than 1'
    assert_equal 200, parser.response.status, 'expected 200 OK from server'
    assert_match "*</html>", parser.response.body.read.chomp

    parser = session.get '/missing_want_404', parse: false
    iterations = 0
    iterations += 1 and parser.parse until parser.ready?
    assert_true iterations > 1, 'number of iterations to parse may vary but should be more than 1'
    assert_equal 404, parser.response.status, 'expected 404 Not Found from server'
  end
